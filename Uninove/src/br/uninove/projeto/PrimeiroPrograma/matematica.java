package br.uninove.projeto.PrimeiroPrograma;

public class matematica {

	int a,b;
	
	public matematica (int x, int y) {
		
		a= x;
		b=y;
		
	}
	
	public int soma() {
		return a + b;
	}
	
	public int subtrai() {
		
		return a - b;
	}
	
	public int multiplica() {
		return a * b;
		
	}
	
	public int divide() {
		
		return a/b;
	}
}
